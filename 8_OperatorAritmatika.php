<?php

$a = 4;
$b = 5;

$result = $a * $b;
var_dump($result);

$result = $a + $b;
var_dump($result);

$resultNegative = $a + $b;
var_dump(-$resultNegative);

$resultNegative = -$result;
var_dump($resultNegative);

$resultModule = 100 % 3;
var_dump($resultModule);

// Catatan
// + untuk penambahan
// - untuk pengurangan
// * untuk perkalian
// / untuk pembagian
